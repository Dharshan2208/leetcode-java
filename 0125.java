class Solution {
    public boolean isPalindrome(String d) {
        // Good idea to use regex to clean all the unneccasary things
        String s = d.toLowerCase().replaceAll("[^a-z0-9]", "");
        int left = 0;
        int right = s.length() - 1;

        while (left < right) {
            if (s.charAt(left) != s.charAt(right)) {
                return false;
            }
            left++;
            right--;
        }

        return true;
    }
}